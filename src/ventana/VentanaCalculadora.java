    private void botonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIgualActionPerformed

        double numero2;

        if (operacion.equals("nula")) {
            etiquetaNumeros.setText(cadenaNumeros);
        } else if (operacion.equals("sumar")) {
            numero2 = Double.parseDouble(cadenaNumeros);
            resultado = numero1 + numero2;
            cadenaNumeros = String.valueOf(resultado);
            parteEntera = (int) (resultado);
            parteDecimal = parteEntera - resultado;
            if (parteDecimal != 0) {
                etiquetaNumeros.setText(String.format("%.2f", resultado));
                etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.2f", resultado));
            } else {
                etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.0f", resultado));
                etiquetaNumeros.setText(String.format("%.0f", resultado));
            }

            operacion = "nula";
            activado = false;
        } else if (operacion.equals("restar")) {
            numero2 = Double.parseDouble(cadenaNumeros);
            resultado = numero1 - numero2;
            cadenaNumeros = String.valueOf(resultado);
            parteEntera = (int) (resultado);
            parteDecimal = parteEntera - resultado;
            if (parteDecimal != 0) {
                etiquetaNumeros.setText(String.format("%.2f", resultado));
                etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.2f", resultado));
            } else {
                etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.0f", resultado));
                etiquetaNumeros.setText(String.format("%.0f", resultado));
            }

            operacion = "nula";
            activado = false;
        } else if (operacion.equals("multiplicar")) {
            numero2 = Double.parseDouble(cadenaNumeros);
            resultado = numero1 * numero2;
            cadenaNumeros = String.valueOf(resultado);
            parteEntera = (int) (resultado);
            parteDecimal = parteEntera - resultado;
            if (parteDecimal != 0) {
                etiquetaNumeros.setText(String.format("%.2f", resultado));
                etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.2f", resultado));
            } else {
                etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.0f", resultado));
                etiquetaNumeros.setText(String.format("%.0f", resultado));
            }

            operacion = "nula";
            activado = false;
        } else if (operacion.equals("division")) {
            numero2 = Double.parseDouble(cadenaNumeros);
            if (numero2 != 0) {
                resultado = numero1 / numero2;
                cadenaNumeros = String.valueOf(resultado);
                parteEntera = (int) (resultado);
                parteDecimal = parteEntera - resultado;
                if (parteDecimal != 0) {
                    etiquetaNumeros.setText(String.format("%.2f", resultado));
                    etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.2f", resultado));
                } else {
                    etiquetaMuestra.setText(etiquetaMuestra.getText() + " = " + String.format("%.0f", resultado));
                    etiquetaNumeros.setText(String.format("%.0f", resultado));
                }

                operacion = "nula";
                activado = false;

            } else {
                resultado = 0f;
                etiquetaMuestra.setText("Error no se puede dividir por cero");
                etiquetaNumeros.setText("Error no se puede dividir por cero");
            };

        }

        numero2 = Double.parseDouble(cadenaNumeros);

        // etiquetaMuestra.setText(" ");
        activado = true;

    }//GEN-LAST:event_botonIgualActionPerformed

