    private void botonRaizCuadradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRaizCuadradaActionPerformed
        if (cadenaNumeros != "") {
            double numero = Double.parseDouble(cadenaNumeros);
            double raiz = Math.sqrt(numero);
            String raizCuadrada = String.valueOf(String.format("%.2f", raiz));
            etiquetaMuestra.setText("\u221A" + cadenaNumeros + " = " + raizCuadrada);
            cadenaNumeros = "";
            etiquetaNumeros.setText("0.");
        } else {
            etiquetaMuestra.setText("Error ...");
        }
    }//GEN-LAST:event_botonRaizCuadradaActionPerformed

